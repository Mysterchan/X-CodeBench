# Предварительно вычислим факториалы и обратные факториалы для вычисления сочетаний по модулю
# Максимум нам нужен для n=4 (кол-во плиток), но для безопасности сделаем до 10
MAX = 10
fact = [1] * (MAX+1)
inv_fact = [1] * (MAX+1)

def modinv(a, m=MOD):
    # Быстрое возведение в степень по модулю (обратный элемент)
    return pow(a, m-2, m)

for i in range(2, MAX+1):
    fact[i] = fact[i-1] * i % MOD
inv_fact[MAX] = modinv(fact[MAX], MOD)
for i in reversed(range(1, MAX)):
    inv_fact[i] = inv_fact[i+1] * (i+1) % MOD

def comb(n, k):
    if k > n or k < 0:
        return 0
    return fact[n] * inv_fact[k] % MOD * inv_fact[n-k] % MOD

