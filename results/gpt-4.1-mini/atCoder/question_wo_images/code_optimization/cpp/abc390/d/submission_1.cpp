#include <bits/stdc++.h>
using namespace std;
#define int long long

int n;
int a[20];
int basis[64]; // basis for XOR space

void insert_vector(int x) {
    for (int i = 63; i >= 0; i--) {
        if ((x >> i) & 1) {
            if (!basis[i]) {
                basis[i] = x;
                return;
            }
            x ^= basis[i];
        }
    }
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    cin >> n;
    int total_xor = 0;
    for (int i = 0; i < n; i++) {
        cin >> a[i];
        total_xor ^= a[i];
    }

    // Insert all a[i] into the basis
    for (int i = 0; i < n; i++) {
        insert_vector(a[i]);
    }

    // Count number of basis vectors
    int rank = 0;
    for (int i = 0; i < 64; i++) {
        if (basis[i]) rank++;
    }

    // Number of possible XOR values = 2^(rank - 1)
    // Because the XOR of all bags is invariant, the possible XOR values form a coset of the subspace generated by the basis excluding the vector total_xor.
    // The dimension of the subspace of possible XOR differences is rank - 1.
    // So total possible XOR values = 2^(rank - 1)

    // Handle the case when rank = 0 (should not happen since n >= 2 and A_i >= 1)
    if (rank == 0) {
        cout << 1 << "\n";
        return 0;
    }

    cout << (1LL << (rank - 1)) << "\n";

    return 0;
}