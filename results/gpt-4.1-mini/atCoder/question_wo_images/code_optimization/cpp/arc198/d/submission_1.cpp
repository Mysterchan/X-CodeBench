struct dsu {
    vector<int> p, sz;
    dsu(int n) : p(n), sz(n, 1) {
        iota(p.begin(), p.end(), 0);
    }
    int get(int x) {
        while (p[x] != x) x = p[x] = p[p[x]];
        return x;
    }
    bool unite(int x, int y) {
        x = get(x);
        y = get(y);
        if (x == y) return false;
        if (sz[x] < sz[y]) swap(x, y);
        p[y] = x;
        sz[x] += sz[y];
        return true;
    }
};

