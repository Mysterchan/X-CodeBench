# 思路分析：
# 操作是选区间 [L,R]，将该区间内所有元素替换为 A_L。
# 操作后序列的不同取决于：
# - 替换区间的左端点 L 选定后，替换值确定为 A_L。
# - 替换区间内所有元素变为 A_L。
# 
# 目标：统计所有可能操作后得到的不同序列数。
#
# 关键观察：
# 1. 操作前的序列本身也算一种结果（不改变任何元素的操作，即 L=R，区间长度1，替换为自身）。
# 2. 替换区间内元素都变为 A_L，区间外元素不变。
# 3. 不同的操作可能产生相同的结果序列。
#
# 解决方案：
# 设定一个方法，统计所有可能的结果序列数。
#
# 由于N最大10^6，不能枚举所有区间。
#
# 观察：
# 对于固定的替换值 v = A_L，替换区间 [L,R] 中所有元素变为 v。
# 替换区间外元素保持不变。
#
# 结果序列的不同，主要由替换区间的左右边界决定。
#
# 进一步分析：
# 设原序列为 A。
# 对于每个位置 i，替换区间左端点为 i，替换值为 A[i]。
# 替换区间右端点 R ≥ i。
#
# 替换区间内元素都变为 A[i]。
#
# 替换区间外元素不变。
#
# 结果序列的不同，取决于替换区间右端点 R。
#
# 由于替换区间内元素都变为 A[i]，替换区间内原本的元素被统一为 A[i]。
#
# 关键是：替换区间右端点 R 的选择会影响替换区间外的元素是否被替换。
#
# 但替换区间外元素不变。
#
# 因此，结果序列的不同，取决于替换区间的长度和替换值。
#
# 进一步简化：
# 对于固定 i，替换区间 [i,R]，替换值为 A[i]。
# 替换区间内元素都变为 A[i]。
#
# 结果序列中，区间 [i,R] 的元素都为 A[i]，区间外元素不变。
#
# 由于替换区间内元素都变为 A[i]，如果原区间内元素已经都是 A[i]，替换后序列不变。
#
# 如果区间内有元素不为 A[i]，替换后序列会改变。
#
# 但题目要求操作必须执行一次，且替换区间长度至少1。
#
# 综上：
# 对于每个 i，考虑所有 R ≥ i，使得替换区间 [i,R] 替换后产生不同序列。
#
# 由于替换区间内元素都变为 A[i]，替换区间内原本不为 A[i] 的元素被改变。
#
# 但替换区间外元素不变。
#
# 结果序列的不同，取决于替换区间的右端点 R。
#
# 由于替换区间内元素都变为 A[i]，替换区间内原本不为 A[i] 的元素被统一替换。
#
# 关键是：替换区间右端点 R 的最大范围受限于区间内元素是否都能被替换为 A[i]。
#
# 但题目没有限制替换区间内元素必须相同，替换区间内元素都被替换为 A[i]，无论原来是什么。
#
# 因此，替换区间右端点 R 可以从 i 到 N。
#
# 但是不同的 R 可能产生相同的结果序列。
#
# 例如，替换区间 [i,R1] 和 [i,R2]，如果区间 [R1+1,R2] 的元素都等于 A[i]，那么替换区间 [i,R1] 和 [i,R2] 替换后序列相同。
#
# 因此，结果序列的不同数目，等价于：
# 对每个 i，统计以 i 为左端点，替换值为 A[i]，能产生多少不同的右端点 R，使得替换后序列不同。
#
# 进一步：
# 替换区间 [i,R] 替换后序列中，区间 [i,R] 全部为 A[i]。
# 区间外元素不变。
#
# 如果区间 [i,R] 右端点 R 增加，但区间 [R1+1,R2] 的元素都等于 A[i]，那么替换区间 [i,R1] 和 [i,R2] 替换后序列相同。
#
# 因此，右端点 R 的不同结果序列对应于区间 [i,R] 中元素不为 A[i] 的位置的分布。
#
# 具体做法：
# 对于每个 i，找到从 i 开始，连续的元素中不为 A[i] 的位置。
# 右端点 R 的变化会导致替换区间内不为 A[i] 的元素数量变化。
#
# 由于替换区间内元素都被替换为 A[i]，替换区间内原本不为 A[i] 的元素被统一替换。
#
# 结果序列的不同，取决于替换区间外元素和替换区间内元素的分布。
#
# 但替换区间外元素不变。
#
# 综上，替换区间右端点 R 的不同导致替换区间内元素被替换的范围不同，从而导致结果序列不同。
#
# 关键是：替换区间右端点 R 的不同，导致替换区间内元素被替换的范围不同。
#
# 由于替换区间内元素都变为 A[i]，替换区间内原本不为 A[i] 的元素被统一替换。
#
# 因此，替换区间右端点 R 的不同，导致替换区间内元素被替换的范围不同。
#
# 但如果区间 [i,R1] 和 [i,R2] 中间的元素都等于 A[i]，那么替换区间 [i,R1] 和 [i,R2] 替换后序列相同。
#
# 结论：
# 对于每个 i，替换区间右端点 R 的不同结果序列对应于区间 [i,R] 中不为 A[i] 的元素的分布。
#
# 具体实现：
# 对于每个 i，找到从 i 开始，连续的元素中不为 A[i] 的位置。
# 右端点 R 的不同结果序列对应于不为 A[i] 的元素的不同分布。
#
# 由于替换区间内元素都变为 A[i]，替换区间内原本不为 A[i] 的元素被统一替换。
#
# 结果序列的不同，取决于替换区间外元素和替换区间内元素的分布。
