# 由于替换区间外元素不变，替换区间内元素都变为 A[i]。
#
# 结果序列的不同，取决于替换区间的左右边界和替换值。
#
# 综上：
# 统计所有可能的结果序列数 = 1 + sum over all pairs (L,R) of distinct sequences generated by operation.
