import sys
sys.setrecursionlimit(10**7)
input = sys.stdin.readline

N = int(input())
edges = [[] for _ in range(N+1)]
for _ in range(N-1):
    a,b = map(int,input().split())
    edges[a].append(b)
    edges[b].append(a)

# 我們要找一個子樹，使得：
# - 是樹（連通且無環）
# - 每個節點度數為1或4
# - 至少有一個節點度數為4
# 
# 注意：子圖是原樹的子集邊和頂點，且必須是樹。
# 
# 度數是子圖中該節點的邊數。
# 
# 問題轉化：
# 找一個子樹，使得每個節點的子樹度數是1或4，且至少有一個度數4。
# 
# 觀察：
# - 度數1的節點是葉子
# - 度數4的節點是內部節點，且度數必須是4
# 
# 因為是樹，度數4的節點必須有4個子節點（子樹邊）
# 
# 我們可以用DP：
# 對每個節點u，定義兩種狀態：
# dp[u][0]: 以u為根的子樹中，u的度數為1的最大子樹大小（若無法則-∞）
# dp[u][1]: 以u為根的子樹中，u的度數為4的最大子樹大小（若無法則-∞）
# 
# 注意：
# - u度數為1時，u必須是葉子（子樹中只有一條邊連接u），
#   因為u是根，度數是子樹中u的邊數。
#   根節點的度數是它子樹中與它相連的邊數。
#   若u度數1，表示u有且只有一個子節點（或是葉子？）
#   但根節點沒有父節點，度數是子節點數。
#   根節點的度數是子節點數。
#   所以度數1表示u有1個子節點。
#   但葉子度數是1是因為它只有一條邊連接父節點。
#   但在子樹中，根節點沒有父節點，度數是子節點數。
#   所以根節點度數是子節點數。
#   因此：
#   - 度數1 => u有1個子節點
#   - 度數4 => u有4個子節點
# 
# 但葉子度數是1是因為它只有一條邊連接父節點。
# 
# 因此，我們要考慮子樹的根節點度數是子節點數。
# 
# 另外，葉子度數1是因為它沒有子節點，只有父節點。
# 
# 但在子樹中，根節點沒有父節點，度數是子節點數。
# 
# 所以根節點度數是子節點數。
# 
# 因此：
# - 若u度數1，表示u有1個子節點
# - 若u度數4，表示u有4個子節點
# 
# 但葉子節點度數是1是因為它只有一條邊連接父節點。
# 
# 這裡的度數是子圖中該節點的邊數。
# 
# 因為子圖是樹，度數是該節點在子圖中的邊數。
# 
# 根節點沒有父節點，度數是子節點數。
# 
# 非根節點度數是子節點數+1（父節點邊）。
# 
# 因此，我們要在整棵樹中找子樹，使得每個節點度數是1或4。
# 
# 但根節點度數是子節點數。
# 
# 非根節點度數是子節點數+1。
# 
# 因此：
# - 根節點度數只能是1或4
# - 非根節點度數只能是1或4
# 
# 對非根節點：
# degree = children_count + 1
# => children_count = degree - 1
# 
# degree只能是1或4
# => children_count = 0 或 3
# 
# 對根節點：
# degree = children_count
# => children_count = 1 或 4
# 
# 總結：
# - 根節點子節點數 = 1 或 4
# - 非根節點子節點數 = 0 或 3
# 
# 且至少有一個節點度數4 => 至少有一個節點子節點數為4(根)或3(非根)
# 
# 轉換成子樹結構：
# - 根節點子節點數為1或4
# - 非根節點子節點數為0或3
# - 至少有一個節點子節點數為4(根)或3(非根)
# 
# 我們用DFS從任意節點當根，計算dp：
# dp[u][is_root] = (max_size, has_4_degree_node)
# 
# is_root = True/False
# 
# 對u的子節點v，我們選擇是否加入v的子樹
# 
# u的子節點數必須是：
# - is_root: 1或4
# - else: 0或3
# 
# 我們要從子節點中選擇k個子樹，使得k符合上述條件，且總大小最大，且至少有一個節點度數4
# 
# dp[v][False]和dp[v][True]都可用，因為v成為子樹根或非根都可
# 
# 但v在u的子樹中，v必須是非根（因為u是根）
# 
# 因此，對u的子節點v，我們只能用dp[v][False]
# 
# 因為u是子樹根，v是u的子節點，v不是根
# 
# 所以：
# dp[u][True] = max over k in {1,4} of sum選k個子節點dp[v][False]的最大和 + 1
# dp[u][False] = max over k in {0,3} of sum選k個子節點dp[v][False]的最大和 + 1
# 
# 且has_4_degree_node = (k==4 or k==3) or any子節點has_4_degree_node
# 
# 
# 實作細節：
# 對每個u，我們先計算所有子節點v的dp[v][False]
# 
# 對dp[v][False]，我們有(max_size, has_4)
# 
# 我們要從子節點中選k個，使得sum max_size最大，且has_4_degree_node為True
# 
# 因為k固定為0,1,3,4，且k不大於子節點數
# 
# 我們先將子節點dp[v][False]排序，依max_size降序
# 
# 對每個k，取前k個子節點的max_size和，判斷has_4_degree_node是否成立
# 
# has_4_degree_node = (k==3 or k==4) or (子節點中至少一個has_4_degree_node為True)
# 
# 但has_4_degree_node必須在子樹中至少有一個度數4節點
# 
# 因為u度數為4或3時，u本身就是度數4節點
# 
# 所以k=3或4時，u本身度數4，has_4_degree_node=True
# 
# k=0或1時，u度數1，has_4_degree_node取決於子節點中是否有
# 
# 
# 另外k=0時，子節點數為0，u度數1不成立，因為根節點度數只能是1或4
# 
# 根節點度數1表示子節點數1
# 
# 所以k=0不適用於根節點
# 
# 
# 總結：
# dp[u][True] k in {1,4}
# dp[u][False] k in {0,3}
# 
# 
# 實作：
# 對u的子節點dp[v][False]排序
# 對k取前k個子節點和
# 判斷has_4_degree_node
# 
# 
# 最後答案是max(dp[r][True].max_size for r in 1..N) if has_4_degree_node else -1
# 
# 
# 注意：
# dp[u][False]表示u非根節點時的最大子樹大小
# dp[u][True]表示u為根節點時的最大子樹大小
# 
# 
# 實作時用-1表示無效狀態
