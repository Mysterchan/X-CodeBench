import sys
sys.setrecursionlimit(10**7)
MOD = 998244353

N = int(sys.stdin.readline())
s = sys.stdin.readline().strip()

# Gの辺は以下の通り
# - 頂点0～N-1で環状に辺がある（N本）
# - s_i=1の頂点iと頂点Nを結ぶ辺がある（M本）

# 頂点0～N-1は環状グラフの頂点
# 頂点Nは特別な頂点

# 各辺は向きを決める（2通り）
# 入次数の組み合わせの個数を求める

# まず、環状の辺はN本で、それぞれの辺はどちら向きにしてもよい
# これだけなら、入次数の組み合わせは2^N通りで、
# 入次数の和はN（各辺は1つの頂点の入次数を1増やす）

# しかし、頂点Nに繋がる辺がM本ある（M = s.count('1')）
# これらの辺も向きを決める（2通り）

# つまり、全体の辺数は N + M
# 向きの決め方は 2^(N+M)

# しかし、入次数の組み合わせの個数はこれより少ない

# 問題の本質は、入次数の組み合わせの個数を求めること

# 重要な性質：
# - 環状の辺は頂点0～N-1間の辺で、各頂点の次数は2（環状なので）
# - 頂点Nの次数はM（s_i=1の数）

# 各辺の向きを決めると、入次数の和は辺の数（N+M）

# 頂点0～N-1の入次数の和は、環状の辺の入次数の和 + 頂点Nに繋がる辺の入次数の和
# 頂点Nの入次数は、s_i=1の辺の向きで決まる

# ここで、環状の辺の向きの決め方は、頂点0～N-1の入次数の組み合わせに影響する

# 環状の辺の向きの組み合わせは2^N通りだが、
# 入次数の組み合わせの個数はN通り（0からNまでの入次数の分布）

# 実は、環状の辺の入次数の組み合わせはN通りで、
# 頂点Nに繋がる辺の向きは独立に決められるので、
# 頂点Nの入次数は0からMまでの整数値をとる

# つまり、入次数の組み合わせの個数は
# 環状の辺の入次数の組み合わせ数 × 頂点Nの入次数の組み合わせ数

# 環状の辺の入次数の組み合わせ数はN通り
# 頂点Nの入次数の組み合わせ数はM+1通り（0からMまで）

# したがって、答えは N * (M+1) mod 998244353

M = s.count('1')
ans = (N * (M + 1)) % MOD
print(ans)