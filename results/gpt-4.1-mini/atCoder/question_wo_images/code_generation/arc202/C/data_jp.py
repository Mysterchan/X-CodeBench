# 1. 前処理: 最小素因数テーブルを作成
spf = [0] * (MAX + 1)
def build_spf():
    spf[1] = 1
    for i in range(2, MAX + 1):
        spf[i] = 0
    for i in range(2, int(MAX**0.5) + 1):
        if spf[i] == 0:
            for j in range(i*i, MAX+1, i):
                if spf[j] == 0:
                    spf[j] = i
    for i in range(2, MAX+1):
        if spf[i] == 0:
            spf[i] = i

