MOD = 998244353

H, W = map(int, input().split())

# Обозначим:
# r_i - максимальный индекс c для строки i, до которого элементы равны 1 (включительно)
# c_j - максимальный индекс r для столбца j, до которого элементы равны 1 (включительно)
# r_i ∈ [0..W], c_j ∈ [0..H]
# Матрица A_{i,j} = 1 тогда и только тогда, когда j ≤ r_i или i ≤ c_j

# Все возможные матрицы соответствуют всем возможным последовательностям r_i и c_j,
# где r_i ∈ [0..W], c_j ∈ [0..H].

# Количество таких матриц: (W+1)^H * (H+1)^W

# Нужно посчитать сумму по всем матрицам суммы элементов.

# Рассмотрим элемент A_{i,j}.
# Он равен 1, если j ≤ r_i или i ≤ c_j.
# Вероятность (в смысле количества) того, что A_{i,j} = 0:
# j > r_i и i > c_j
# => r_i < j и c_j < i

# Кол-во вариантов r_i: W+1
# Кол-во вариантов c_j: H+1

# Кол-во вариантов r_i < j: j (т.к. r_i ∈ [0..W], r_i < j значит r_i ∈ [0..j-1], всего j вариантов)
# Кол-во вариантов c_j < i: i (аналогично)

# Кол-во вариантов r_i ≥ j: (W+1) - j
# Кол-во вариантов c_j ≥ i: (H+1) - i

# Кол-во вариантов r_i и c_j вместе: (W+1)*(H+1)

# Кол-во вариантов, где A_{i,j} = 0:
# r_i < j и c_j < i => j * i

# Кол-во вариантов, где A_{i,j} = 1:
# (W+1)*(H+1) - j*i

# Теперь, поскольку r_i и c_j независимы по i и j, общее количество матриц:
total = pow(W+1, H, MOD) * pow(H+1, W, MOD) % MOD

# Но мы считаем сумму по всем матрицам суммы элементов.
# Для каждого элемента (i,j) сумма значений A_{i,j} по всем матрицам равна количеству матриц, где A_{i,j} = 1.

# Кол-во матриц, где A_{i,j} = 1:
# (W+1)^H * (H+1)^W - j*i * (W+1)^{H-1} * (H+1)^{W-1}
# Почему?
# Потому что r_i и c_j независимы, и для фиксированного i,j:
# - r_i имеет (W+1) вариантов, из них j дают r_i < j
# - c_j имеет (H+1) вариантов, из них i дают c_j < i
# Для остальных r_k (k≠i) и c_l (l≠j) вариантов (W+1)^{H-1} и (H+1)^{W-1} соответственно.

# Таким образом:
# Кол-во матриц с A_{i,j} = 0 = j * i * (W+1)^{H-1} * (H+1)^{W-1}
# Кол-во матриц с A_{i,j} = 1 = total - j * i * (W+1)^{H-1} * (H+1)^{W-1}

# Суммируем по всем i,j:
# sum_{i=1}^H sum_{j=1}^W [total - j*i*(W+1)^{H-1}*(H+1)^{W-1}]
# = H*W*total - (W+1)^{H-1}*(H+1)^{W-1} * (sum_{i=1}^H i) * (sum_{j=1}^W j)

# Вычислим суммы:
sum_i = H*(H+1)//2
sum_j = W*(W+1)//2

pow_r = pow(W+1, H-1, MOD)
pow_c = pow(H+1, W-1, MOD)

ans = (H*W*total - pow_r * pow_c * sum_i * sum_j) % MOD

print(ans)