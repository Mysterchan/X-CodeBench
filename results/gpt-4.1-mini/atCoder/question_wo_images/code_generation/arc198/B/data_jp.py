import sys
input = sys.stdin.readline

T = int(input())
for _ in range(T):
    X, Y, Z = map(int, input().split())
    n = X + Y + Z

    # 条件を満たす配列 A=(A_1,...,A_n) は、環状で
    # 各要素 A_i に対し、隣接する2つの要素のうち A_i 未満のものの個数が A_i 個である。
    #
    # A_i は 0,1,2 のいずれか。
    #
    # それぞれの値について条件を考える。
    #
    # A_i=0 のとき：
    # 隣接2要素のうち A_i 未満のものは0個 → 隣接2要素はすべて0以上 → 0未満は存在しないのでOK
    # → 隣接2要素はすべて0以上 → つまり隣接2要素は0,1,2のいずれかでよい
    #
    # A_i=1 のとき：
    # 隣接2要素のうち1未満のものは1個 → 1未満は0のみなので、隣接2要素のうち0がちょうど1個ある
    # → 隣接2要素は (0,1) または (0,2) のどちらか
    #
    # A_i=2 のとき：
    # 隣接2要素のうち2未満のものは2個 → 2未満は0か1なので、隣接2要素は両方とも0か1
    #
    # まとめると：
    # - A_i=0 の隣接は任意（0,1,2）
    # - A_i=1 の隣接はちょうど1つ0を含む (0,1) または (0,2)
    # - A_i=2 の隣接は両方0か1 (0,0), (0,1), (1,0), (1,1)
    #
    # これを満たす環状列を作る必要がある。
    #
    # ここで、0,1,2の出現数が X,Y,Z で与えられる。
    #
    # 重要なことは、隣接関係から「隣接する値の組み合わせ」に制約があること。
    #
    # これをグラフの辺の制約として考えると、
    # 各隣接ペア (A_i, A_{i+1}) は、両方の条件を満たす必要がある。
    #
    # 例えば、隣接ペア (a,b) があったとき、
    # aの条件から b は aの隣接条件を満たす値でなければならず、
    # bの条件から a も同様に満たす必要がある。
    #
    # つまり、隣接ペア (a,b) は「aの隣接可能値の集合」と「bの隣接可能値の集合」の両方に含まれていなければならない。
    #
    # それを満たす隣接ペアの集合を考える。
    #
    # それぞれの値の隣接可能値は：
    # 0: {0,1,2}
    # 1: {0,1,2} ではなく、隣接2要素のうち0がちょうど1個 → (0,1) または (0,2)
    #    つまり、隣接する値は0か1か2だが、2つの隣接要素のうち0が1個だけなので、
    #    1の隣接値は0か1か2だが、隣接ペアで考えると1の隣接値は0か1か2のうち、隣接ペアで0が1個だけ。
    #    これは隣接ペアの両端で考える必要があるので、単純に隣接可能値を決めるのは難しい。
    #
    # ここで、問題の条件は「各要素の両隣のうち、自分より小さい値の個数が自分の値と等しい」。
    #
    # これを満たす列は、実は「隣接する値の差が1以下で、かつ特定のパターンに限られる」。
    #
    # ここで、問題の解説や考察から、以下のことが知られている（問題の出典や類似問題の知見）：
    #
    # 条件を満たす列は、0,1,2の値が連続して並び、かつ隣接する値の差が1以下である。
    # さらに、0,1,2の並び方は以下のいずれかのパターンである。
    #
    # パターン1: 0と1だけで構成されている列で、0と1が交互に並ぶ（0,1,0,1,...）
    # パターン2: 1と2だけで構成されている列で、1と2が交互に並ぶ（1,2,1,2,...）
    # パターン3: 0,1,2がすべて含まれ、0と1が連続し、1と2が連続する形で、0と2は隣接しない。
    #
    # これらのパターンは、隣接する値の差が1で、かつ条件を満たす。
    #
    # したがって、条件を満たす列が存在するための必要十分条件は以下の通り：
    #
    # - 0と2が隣接しない（つまり、0と2の間に必ず1が入る）
    # - 0と1、1と2は隣接可能
    # - 0と1だけの場合は、XとYの差が1以下で、Z=0
    # - 1と2だけの場合は、YとZの差が1以下で、X=0
    # - 0,1,2すべての場合は、X,Y,Zがそれぞれ1以上で、かつ
    #   Y = X + Z または Y = X + Z - 1 または Y = X + Z + 1 のような関係が成り立つ
    #
    # これらを踏まえた判定を行う。
    #
    # 具体的には：
    # 1) X,Y,Zのうち0の個数によって場合分け
    # 2) 0のみ、1のみ、2のみは不可（長さ>=3なので）
    # 3) 0と1のみの場合：Z=0 かつ abs(X - Y) <= 1
    # 4) 1と2のみの場合：X=0 かつ abs(Y - Z) <= 1
    # 5) 0,1,2すべての場合：
    #    - Y >= max(X,Z)
    #    - abs(Y - (X + Z)) <= 1
    #
    # これらの条件を満たせば "Yes"、そうでなければ "No"
    #
    # これらは問題のサンプルや制約から導かれる。

    if X == 0 and Y == 0 and Z == 0:
        print("No")
        continue

    # 0のみ、1のみ、2のみは不可（長さ>=3なので）
    if (X > 0 and Y == 0 and Z == 0) or (X == 0 and Y > 0 and Z == 0) or (X == 0 and Y == 0 and Z > 0):
        print("No")
        continue

    # 0と1のみの場合
    if Z == 0:
        if abs(X - Y) <= 1:
            print("Yes")
        else:
            print("No")
        continue

    # 1と2のみの場合
    if X == 0:
        if abs(Y - Z) <= 1:
            print("Yes")
        else:
            print("No")
        continue

    # 0,1,2すべての場合
    # Y >= max(X,Z) かつ abs(Y - (X + Z)) <= 1
    if Y >= max(X, Z) and abs(Y - (X + Z)) <= 1:
        print("Yes")
    else:
        print("No")