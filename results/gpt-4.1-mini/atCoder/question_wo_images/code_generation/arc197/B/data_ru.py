import sys
input = sys.stdin.readline

T = int(input())
for _ in range(T):
    N = int(input())
    A = list(map(int, input().split()))
    A.sort()
    prefix_sum = [0]
    for v in A:
        prefix_sum.append(prefix_sum[-1] + v)

    # Мы хотим найти максимальное k (1 <= k < N), такое что
    # среднее последних k элементов > среднее всех выбранных элементов.
    # Рассмотрим подпоследовательность из последних k элементов (самых больших),
    # и возможно добавим минимальный элемент (самый маленький) для увеличения среднего.
    #
    # Пусть:
    # - min_val = A[0]
    # - sum_k = сумма последних k элементов = prefix_sum[N] - prefix_sum[N-k]
    # - среднее подпоследовательности из min_val и последних k элементов:
    #   avg = (min_val + sum_k) / (k+1)
    #
    # Кол-во элементов строго больше avg — это ровно k (т.к. все последние k элементов > min_val).
    #
    # Условие: каждый из последних k элементов > avg
    # Проверим для минимального из них (A[N-k]) условие:
    # A[N-k] > avg = (min_val + sum_k) / (k+1)
    # (k+1)*A[N-k] > min_val + sum_k
    #
    # Переберём k от 1 до N-1 и найдём максимальный k, для которого условие выполняется.
    #
    # Если ни для какого k условие не выполняется, ответ 0 (например, все элементы равны).

    min_val = A[0]
    ans = 0
    for k in range(1, N):
        sum_k = prefix_sum[N] - prefix_sum[N - k]
        if (k + 1) * A[N - k] > min_val + sum_k:
            ans = k
    print(ans)