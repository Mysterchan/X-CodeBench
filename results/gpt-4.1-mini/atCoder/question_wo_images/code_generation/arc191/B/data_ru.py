import sys
input = sys.stdin.readline

T = int(input())
for _ in range(T):
    N, K = map(int, input().split())
    # Проверим условие совместимости:
    # X XOR N == X % N
    # Пусть q = X // N, r = X % N, тогда X = q*N + r
    # Условие: (q*N + r) XOR N == r
    # Раскроем XOR:
    # (q*N + r) XOR N = (q*N) XOR N XOR r (т.к. XOR ассоциативен и коммутативен)
    # Но q*N и r имеют разные биты (r < N), поэтому XOR(q*N + r, N) = XOR(q*N, N) XOR r
    # Условие: XOR(q*N, N) XOR r == r
    # Значит XOR(q*N, N) == 0
    # Значит q*N == N (т.к. XOR(q*N, N) = 0 => q*N = N)
    # Но q*N = N*q, значит XOR(N*q, N) = 0
    # Рассмотрим XOR(N*q, N) = 0
    # Это возможно, если и только если N*q == N (т.к. XOR(a,a)=0)
    # Но q*N = N*q, XOR(N*q, N) = 0 => N*q = N
    # Значит q=1
    #
    # Проверим на примерах:
    # Для N=2:
    # X=2: q=1, r=0
    # XOR(2,2)=0 == 0 -> совместимо
    # X=3: q=1, r=1
    # XOR(3,2)=1 == 1 -> совместимо
    #
    # Для X = q*N + r, условие совместимости:
    # XOR(q*N, N) == 0
    # XOR(q*N, N) = 0 <=> q*N == N (т.к. XOR(a,a)=0)
    # Значит q*N = N => q=1
    #
    # Следовательно, q=1, r от 0 до N-1
    # X = N + r, r in [0, N-1]
    # Проверим условие для r:
    # XOR(X, N) = XOR(N + r, N) = XOR(N, N) XOR r = 0 XOR r = r
    # X % N = r
    # Значит условие выполняется для всех X = N + r, r in [0, N-1]
    #
    # Таким образом, все совместимые числа X имеют вид:
    # X = N + r, где r = 0,1,...,N-1
    #
    # Количество таких чисел: N
    #
    # Нужно найти K-е по величине такое число:
    # Совместимые числа: N, N+1, ..., N+(N-1) = 2N-1
    # K-е по величине: N + (K-1)
    #
    # Если K > N, ответ -1

    if K > N:
        print(-1)
    else:
        print(N + (K - 1))