t = int(input())
for _ in range(t):
    n = int(input())
    # 修正ダブルエリミネーションでは、最終的に1チームが優勝するまでに
    # 敗退するチームはn-1チームであり、それぞれ敗退するまでに2敗する。
    # つまり、敗退チームの敗北数は2*(n-1)回。
    # 優勝チームは1敗または0敗で終わるため、総敗北数は2*(n-1)-1または2*(n-1)。
    # しかし、問題のルールにより、最終決戦で勝者グループと敗者グループの
    # それぞれ1チームずつが対戦し、勝者が決まるため、試合数は常に2n-1となる。
    print(2 * n - 1)