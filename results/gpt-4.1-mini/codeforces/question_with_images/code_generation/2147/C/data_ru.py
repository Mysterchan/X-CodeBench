import sys
input = sys.stdin.readline

t = int(input())
for _ in range(t):
    n = int(input())
    s = input().strip()

    # Если нет пустых горшков, ответ YES
    if '0' not in s:
        print("YES")
        continue

    # Если все горшки пустые, ответ YES
    if '1' not in s:
        print("YES")
        continue

    # Проверим, есть ли два соседних пустых горшка
    # Если есть, то можно посадить кроликов смотрящих в разные стороны,
    # чтобы избежать прыжков
    # Если нет, то все пустые горшки изолированы цветами,
    # и кролики будут смотреть либо в сторону границы, либо в сторону цветка,
    # что не создаст конфликтов.
    # Но если пустые горшки идут подряд, то нужно проверить,
    # можно ли расставить направления без конфликтов.

    # На самом деле, из анализа задачи и примеров следует:
    # Ответ NO только если есть хотя бы один пустой горшок,
    # и при этом нет двух соседних пустых горшков,
    # и при этом пустые горшки не на границах.
    # В противном случае ответ YES.

    # Проверим, есть ли два соседних '0'
    if '00' in s:
        print("YES")
        continue

    # Если нет двух соседних пустых горшков,
    # проверим, есть ли пустой горшок на границе
    if s[0] == '0' or s[-1] == '0':
        print("YES")
        continue

    # Иначе ответ NO
    print("NO")