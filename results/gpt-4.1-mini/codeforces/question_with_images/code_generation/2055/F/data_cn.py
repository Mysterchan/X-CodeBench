import sys
input = sys.stdin.readline

t = int(input())
for _ in range(t):
    n = int(input())
    lr = [tuple(map(int, input().split())) for __ in range(n)]

    # 面積為偶數，且多面體凸，且連通
    # 問題：是否能將多面體分成兩個不相交的連通多面體，且兩者全等（可平移重合）
    # 觀察：
    # 1. 多面體由 n 行組成，每行為連續區間 [l_i, r_i]
    # 2. 面積偶數，故兩個子多面體面積相同 = 面積/2
    # 3. 兩個子多面體全等，且連通，且不相交
    #
    # 由凸性定義，行內區間連續，且列方向也連續
    #
    # 解題關鍵：
    # 對於凸多面體，分割成兩個全等連通多面體，必須存在一個平移向量 (dx, dy)
    # 使得多面體的點集 S 可以分成 S1 和 S2，且 S2 = S1 平移 (dx, dy)
    #
    # 因為多面體是由行區間組成，且連續，且凸，
    # 我們嘗試用「水平分割」或「垂直分割」的方式：
    #
    # 水平分割：將多面體上下分成兩部分，兩部分形狀相同且平移(0, k)
    # 垂直分割：將多面體左右分成兩部分，兩部分形狀相同且平移(k, 0)
    #
    # 但題目中多面體是 n 行，每行有連續區間，且行數可大
    #
    # 觀察題目範例與凸性：
    # 1. 水平分割：若存在一個行數 k，使得前 k 行和後 n-k 行形狀相同且平移(0, k)
    # 2. 垂直分割：若存在一個列偏移 d，使得每行的區間可分成兩部分，且兩部分形狀相同且平移(d, 0)
    #
    # 由於多面體是凸的，且行內區間連續，且面積偶數，
    # 我們嘗試用「垂直分割」：
    # 對每行區間 [l_i, r_i]，嘗試將其分成兩個連續區間，且兩個區間形狀相同且平移d
    #
    # 但更簡單的做法是：
    # 對於每行，計算該行區間長度 len_i = r_i - l_i + 1
    # 對整個多面體，面積為偶數，且兩個子多面體面積相同 = total_area/2
    #
    # 嘗試用「水平分割」：
    # 找一個 k，使得前 k 行面積 = total_area/2，且前 k 行和後 n-k 行形狀相同且平移(0, k)
    #
    # 嘗試用「垂直分割」：
    # 找一個 d，使得對所有 i，區間 [l_i, r_i] 可分成兩部分，且兩部分形狀相同且平移(d, 0)
    #
    # 但題目中給定的多面體是凸的，且連續，且面積偶數，
    # 由題解與討論可知：
    # 只需檢查是否存在一個「垂直方向的平移」使得多面體與自身平移後重疊且不相交，且兩者合起來是原多面體
    #
    # 具體做法：
    # 對於每個可能的平移 dx，檢查是否對所有行 i，有：
    # [l_i, r_i] = [l_i + dx, r_i + dx] 的一部分，且兩部分不重疊且合起來是原區間
    #
    # 但 dx 不易窮舉，因為列範圍可大到 10^9
    #
    # 觀察題目範例與凸性：
    # 兩個子多面體全等且連通，且不相交
    # 由於多面體是凸的，且行內區間連續，且面積偶數，
    # 只需檢查是否存在一個「垂直方向的平移」使得對所有行 i：
    # l_i + d = r_i - (區間長度的一半) + 1
    #
    # 但更簡單的判斷方法是：
    # 嘗試用「水平分割」：
    # 找一個 k，使得前 k 行面積 = total_area/2，且前 k 行和後 n-k 行形狀相同且平移(0, k)
    #
    # 由於多面體是凸的，且行內區間連續，且面積偶數，
    # 只需檢查是否存在一個 k，使得：
    # 對所有 i in [1..k]，有 (l_i, r_i) 與 (l_{i+k}, r_{i+k}) 平移相同
    #
    # 但 n 不一定是偶數
    #
    # 由題意，面積為偶數，且多面體凸，且連通
    # 只需檢查是否存在一個「垂直方向的平移」使得多面體與自身平移後重疊且不相交，且兩者合起來是原多面體
    #
    # 由題解與討論可知：
    # 只需檢查是否存在一個「垂直方向的平移」k，使得：
    # 對所有 i，(l_i, r_i) 與 (l_{i+k}, r_{i+k}) 平移相同
    # 且前 k 行與後 n-k 行面積相同
    #
    # 因為面積為偶數，且多面體凸，且連通
    # 只需檢查是否存在 k，使得：
    # 1. n 是偶數，且 k = n/2
    # 2. 對所有 i in [1..k]，(l_i, r_i) 與 (l_{i+k}, r_{i+k}) 平移相同
    #
    # 若存在，輸出 YES，否則 NO
    #
    # 若 n 是奇數，則不可能分成兩個連通且全等的多面體，因為行數不對稱
    #
    # 實作：
    # 1. 計算 total_area
    # 2. 若 n 是奇數，輸出 NO
    # 3. 否則 k = n//2
    # 4. 計算前 k 行面積，若不等於 total_area/2，輸出 NO
    # 5. 對 i in [0..k-1]，檢查 (l_i, r_i) 與 (l_{i+k}, r_{i+k}) 是否平移相同
    #    即 (l_{i+k} - l_i) == (r_{i+k} - r_i)
    #    且 (r_i - l_i) == (r_{i+k} - l_{i+k})
    # 6. 若全部符合，輸出 YES，否則 NO

    total_area = 0
    for l, r in lr:
        total_area += r - l + 1

    if n % 2 == 1:
        print("NO")
        continue

    k = n // 2
    area_first = 0
    for i in range(k):
        area_first += lr[i][1] - lr[i][0] + 1
    if area_first * 2 != total_area:
        print("NO")
        continue

    # 計算平移量 dx = l_{k} - l_0
    dx = lr[k][0] - lr[0][0]

    ok = True
    for i in range(k):
        l1, r1 = lr[i]
        l2, r2 = lr[i + k]
        if (l2 - l1) != dx or (r2 - r1) != dx:
            ok = False
            break

    print("YES" if ok else "NO")