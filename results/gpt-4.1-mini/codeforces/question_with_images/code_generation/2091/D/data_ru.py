import sys
input = sys.stdin.readline

t = int(input())
for _ in range(t):
    n, m, k = map(int, input().split())
    # Минимальная длина самой длинной парты - это минимальное L,
    # при котором можно разместить k столов в n рядах,
    # если в каждом ряду длина парты не превышает L.
    #
    # В каждом ряду можно разместить не более m столов,
    # но при ограничении длины парты L, максимальное количество столов в ряду,
    # если разбивать на группы длины не более L, не ограничено, 
    # т.к. столы могут быть разбиты на несколько парт.
    #
    # Однако, условие задачи: длина самой длинной парты не больше L,
    # значит, в одном ряду можно разместить столы, разбитые на несколько парт,
    # каждая длиной не более L.
    #
    # Максимальное количество столов в ряду при длине парты L - m (все места заняты).
    #
    # Но важен момент: длина самой длинной парты - максимальная длина подряд идущих занятых мест.
    #
    # Чтобы минимизировать максимальную длину парты, нужно разбить k столов на n рядов,
    # так чтобы в каждом ряду максимальная длина подряд идущих занятых мест не превышала L.
    #
    # При фиксированном L, максимальное количество столов, которое можно разместить в одном ряду,
    # разбивая на группы длины не более L, равно m (все места заняты).
    #
    # Но если мы хотим минимизировать L, то нам нужно проверить, можно ли разместить k столов,
    # разбивая их на n рядов, так чтобы в каждом ряду максимальная длина подряд идущих столов <= L.
    #
    # При длине L, максимальное количество столов в одном ряду - m (все места заняты).
    #
    # Но если длина L меньше m, то в одном ряду можно разместить столы, разбитые на группы длины L,
    # то есть максимальное количество столов в ряду - m (все места).
    #
    # Значит, при любом L >= 1, максимальное количество столов в ряду - m.
    #
    # Тогда условие сводится к тому, что k столов нужно разместить в n рядах,
    # при этом максимальная длина подряд идущих столов в ряду не больше L.
    #
    # При длине L, максимальное количество столов в ряду - m.
    #
    # Но если длина L меньше m, то в ряду можно разбить столы на группы длины L,
    # то есть максимальная длина подряд идущих столов - L.
    #
    # Значит, при длине L, максимальное количество столов в ряду - m.
    #
    # Тогда, чтобы разместить k столов, нужно, чтобы:
    # n * m >= k (условие задачи, всегда верно)
    #
    # Но нам нужно минимизировать L, при котором можно разместить k столов,
    # разбивая их на группы длины не более L.
    #
    # При длине L, максимальное количество столов в ряду - m.
    #
    # Но если длина L меньше m, то максимальное количество столов в ряду - m.
    #
    # Значит, условие сводится к тому, что максимальная длина L должна быть не меньше ceil(k / n),
    # потому что если в каждом ряду максимальная длина подряд идущих столов L,
    # то в одном ряду можно разместить не более L столов подряд,
    # а столы в ряду могут быть разбиты на несколько таких групп.
    #
    # Но длина самой длинной парты - максимальная длина подряд идущих столов,
    # значит, чтобы разместить k столов в n рядах, минимальная длина L >= ceil(k / n).
    #
    # При этом L не может быть больше m, так как в ряду всего m мест.
    #
    # Итог: минимальная длина самой длинной парты = min(m, ceil(k / n))
    
    from math import ceil
    ans = min(m, (k + n - 1) // n)
    print(ans)