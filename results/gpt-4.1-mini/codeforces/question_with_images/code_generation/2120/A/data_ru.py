t = int(input())
for _ in range(t):
    l1, b1, l2, b2, l3, b3 = map(int, input().split())

    # Проверяем все варианты укладки трёх прямоугольников без поворота,
    # чтобы получить квадрат со стороной S.

    # Возможные варианты укладки:
    # 1) Все три прямоугольника расположены в ряд по длине (горизонтально):
    #    Тогда высота должна быть одинаковой и равна b1 == b2 == b3,
    #    а сумма длин должна быть равна b1 (т.к. квадрат).
    # 2) Все три прямоугольника расположены в ряд по ширине (вертикально):
    #    Тогда длина должна быть одинаковой и равна l1 == l2 == l3,
    #    а сумма ширин должна быть равна l1.
    # 3) Два прямоугольника расположены рядом, а третий сверху или снизу,
    #    образуя квадрат. Нужно проверить все перестановки и варианты.

    # Поскольку повороты запрещены, размеры фиксированы.

    # Для удобства создадим список прямоугольников
    rects = [(l1, b1), (l2, b2), (l3, b3)]

    def check():
        # Проверка варианта 1: все по высоте равны, сумма длин равна высоте
        if b1 == b2 == b3:
            s = b1
            if l1 + l2 + l3 == s:
                return True

        # Проверка варианта 2: все по длине равны, сумма ширин равна длине
        if l1 == l2 == l3:
            s = l1
            if b1 + b2 + b3 == s:
                return True

        # Проверка варианта 3: два прямоугольника в ряд, третий сверху или снизу
        # Перебираем все перестановки прямоугольников
        from itertools import permutations
        for r in permutations(rects):
            (L1, B1), (L2, B2), (L3, B3) = r

            # Случай 3.1: два прямоугольника расположены горизонтально рядом,
            # третий сверху или снизу, образуя квадрат.
            # Проверяем если высота первого и второго равна, и длина третьего равна сумме длин первых двух,
            # а сумма высот третьего и высоты первых двух равна длине третьего.

            # Вариант 3.1: (r1 и r2 в ряд по длине), r3 сверху или снизу
            if B1 == B2:
                s = L1 + L2
                if L3 == s and B3 + B1 == s:
                    return True

            # Вариант 3.2: (r1 и r2 в ряд по ширине), r3 слева или справа
            if L1 == L2:
                s = B1 + B2
                if B3 == s and L3 + L1 == s:
                    return True

        return False

    print("YES" if check() else "NO")