import sys
input = sys.stdin.readline

MOD = 10**9 + 7

t = int(input())
for _ in range(t):
    a, b, k = map(int, input().split())
    # Анализ задачи:
    # Ариан выигрывает, если в любой матрице размера n x m с элементами от 1 до k
    # найдется подматрица (полученная удалением строк и/или столбцов) размера a x b,
    # все элементы которой равны.
    #
    # Харшит играет оптимально, пытаясь избежать такой подматрицы.
    #
    # Задача сводится к поиску минимальной пары (n,m) (лекс. минимальной),
    # при которой невозможно избежать подматрицы a x b с одинаковыми элементами.
    #
    # Рассмотрим:
    # - Подматрица получается удалением строк и/или столбцов, то есть выбираются
    #   a строк и b столбцов из n и m соответственно.
    # - Чтобы не было подматрицы a x b с одинаковыми элементами, Харшит должен
    #   раскрасить матрицу так, чтобы в любой выбранной a строках и b столбцах
    #   не было одинакового элемента.
    #
    # Известный подход (аналог задачи о раскраске и подматрицах):
    # Минимальное n и m, при которых Ариан гарантированно выигрывает:
    #
    # n = a + (k-1)*(a-1)
    # m = b + (k-1)*(b-1)
    #
    # Обоснование:
    # Харшит может попытаться "разделить" матрицу на k блоков по строкам и столбцам,
    # чтобы избежать подматрицы a x b с одинаковыми элементами.
    # Если n < a + (k-1)*(a-1), то Харшит может "разбить" строки на k групп,
    # каждая из которых меньше a, чтобы избежать подматрицы.
    # Аналогично для столбцов.
    #
    # Проверка на лексикографический минимум:
    # При фиксированных a,b,k формула даёт минимальные n,m.
    #
    # Выводим n и m по модулю 10^9+7.

    n = (a + (k - 1) * (a - 1)) % MOD
    m = (b + (k - 1) * (b - 1)) % MOD
    print(n, m)