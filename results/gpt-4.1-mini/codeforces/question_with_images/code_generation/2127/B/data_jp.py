import sys
input = sys.stdin.readline

t = int(input())
for _ in range(t):
    n, x = map(int, input().split())
    s = input().rstrip()

    # ハミッドの初期位置はx-1（0-indexed）
    pos = x - 1

    # 左端から最初の空セルまでの距離
    left_dist = pos
    # 右端から最初の空セルまでの距離
    right_dist = n - 1 - pos

    # 左側に空セルがあるか
    left_empty = '.' in s[:pos]
    # 右側に空セルがあるか
    right_empty = '.' in s[pos+1:]

    # マニは毎日壁を1つ建てる。ハミッドは毎日壁を1つ破壊して移動できる。
    # ハミッドは脱出までの日数を最小化し、マニは最大化する。
    # 脱出は壁がない方向に移動すること。
    # マニは壁を建てることで脱出方向を塞ぐことができるが、
    # ハミッドは壁を破壊しながら移動できる。

    # 脱出に必要な日数は、ハミッドが壁を破壊しながら端まで移動するのにかかる日数。
    # マニは壁を建てて脱出を遅らせるが、壁を建てられるのは空セルのみで、
    # ハミッドのいるセルには建てられない。

    # したがって、ハミッドが脱出するまでに必要な日数は、
    # 左端または右端までの距離のうち、壁がない方向の距離に依存する。

    # もし左側に空セルがあれば、マニは左側の空セルに壁を建てて脱出を塞げる。
    # 同様に右側も同じ。
    # マニは壁を建てて脱出を塞ぐが、ハミッドは壁を破壊しながら移動できるので、
    # 脱出までの日数は、ハミッドが壁を破壊しながら端まで移動するのにかかる日数。

    # つまり、ハミッドが脱出するまでにかかる日数は、
    # 左端または右端までの距離のうち、壁がない方向の距離の最小値 + 1（初日）

    # ただし、壁がない方向にすぐ脱出できる場合は1日で脱出可能。

    # 具体的には、左端からposまでの間に壁がない場合は左から脱出可能（1日）
    # 右端からposまでの間に壁がない場合は右から脱出可能（1日）

    # そうでなければ、壁を破壊しながら移動する必要があるので、
    # 左端または右端までの距離のうち、壁がない方向の距離 + 1日

    # しかし、問題の条件から、壁がない方向にすぐ脱出できる場合は1日で脱出可能。

    # したがって、左端からposまでの間に壁がないかチェック
    left_clear = all(c == '.' for c in s[:pos])
    right_clear = all(c == '.' for c in s[pos+1:])

    if left_clear or right_clear:
        print(1)
        continue

    # 左右どちらかに空セルがある場合、マニは壁を建てて塞ぐことができるので、
    # ハミッドは壁を破壊しながら移動する必要がある。
    # 脱出までの日数は、左端または右端までの距離のうち、壁がない方向の距離 + 1

    # ただし、壁がある方向にしか空セルがない場合は、その方向に移動するしかない。

    # 左右の空セルの距離を計算
    # 左側の空セルの最も近い位置
    left_pos = -1
    for i in range(pos - 1, -1, -1):
        if s[i] == '.':
            left_pos = i
            break
    # 右側の空セルの最も近い位置
    right_pos = -1
    for i in range(pos + 1, n):
        if s[i] == '.':
            right_pos = i
            break

    # 左側に空セルがある場合の脱出にかかる日数
    left_days = pos - left_pos + 1 if left_pos != -1 else 10**9
    # 右側に空セルがある場合の脱出にかかる日数
    right_days = right_pos - pos + 1 if right_pos != -1 else 10**9

    print(min(left_days, right_days))