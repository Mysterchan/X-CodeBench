import sys
input = sys.stdin.readline

def can_cover(w, h, a, b, x1, y1, x2, y2):
    # 已放置的兩塊板材覆蓋的區域（板材尺寸固定為 a*b）
    # 板材不能旋轉，且不能重疊，且已知兩塊板材不重疊且部分覆蓋屋頂
    # 我們要判斷是否能在不移動這兩塊板材的情況下，用相同尺寸的板材覆蓋整個屋頂
    
    # 思路：
    # 1. 板材不能旋轉，且尺寸固定為 a*b。
    # 2. 板材可以超出屋頂邊界。
    # 3. 已放置的兩塊板材不重疊且部分覆蓋屋頂。
    # 4. 我們要判斷是否能用若干塊 a*b 板材（包含已放置的兩塊）覆蓋整個 w*h 屋頂。
    #
    # 關鍵點：
    # - 板材不能旋轉，故覆蓋屋頂的方式只能是將屋頂分割成若干 a*b 的格子。
    # - 板材可以超出屋頂邊界，故只要能用整數個 a*b 板材在 x 和 y 方向上覆蓋整個屋頂即可。
    # - 兩塊已放置板材的位置固定，且不重疊。
    #
    # 由於板材不能旋轉，覆蓋方式只能是將屋頂分成若干 a*b 的格子，且每個格子放一塊板材。
    # 因此，屋頂的寬度 w 必須被 a 整除，且高度 h 必須被 b 整除，才能完全覆蓋。
    # 但題目中並未要求板材必須完全在屋頂內，板材可以超出屋頂邊界。
    #
    # 因此，我們可以嘗試在 x 方向上用 n_x 塊板材覆蓋，n_x = ceil(w / a)
    # 在 y 方向上用 n_y 塊板材覆蓋，n_y = ceil(h / b)
    #
    # 總共需要 n_x * n_y 塊板材。
    #
    # 現在已放置兩塊板材，位置固定，且不重疊。
    # 我們要判斷是否存在一組 n_x * n_y 個 a*b 板材的格子排布，使得這兩塊板材的位置都在這些格子中，
    # 且這些格子覆蓋整個屋頂。
    #
    # 由於板材不能旋轉，且位置固定，我們可以嘗試將屋頂分割成 n_x * n_y 格子，
    # 每個格子左下角座標為 (i*a, j*b)，i=0..n_x-1, j=0..n_y-1。
    #
    # 兩塊已放置板材的左下角座標分別為 (x1, y1), (x2, y2)。
    # 我們要判斷是否存在一組格子排布，使得 (x1, y1) 和 (x2, y2) 都是格子左下角座標。
    #
    # 由於板材不能旋轉，且格子固定大小，我們只要判斷是否存在整數 i1, j1, i2, j2 使得：
    # x1 = i1 * a, y1 = j1 * b
    # x2 = i2 * a, y2 = j2 * b
    # 且 0 <= i1, i2 < n_x, 0 <= j1, j2 < n_y
    #
    # 但題目中 x1, y1, x2, y2 可能為負數或超出屋頂範圍，因為板材可以超出屋頂邊界。
    # 因此，我們只要判斷是否存在整數 i1, j1, i2, j2 使得：
    # x1 = i1 * a
    # y1 = j1 * b
    # x2 = i2 * a
    # y2 = j2 * b
    #
    # 即 x1 和 x2 必須是 a 的整數倍，y1 和 y2 必須是 b 的整數倍。
    #
    # 如果上述條件成立，則可以將屋頂分割成 n_x * n_y 格子，且兩塊板材都在格子中，
    # 剩下的格子可以用其他板材覆蓋，故答案為 "Yes"。
    #
    # 否則，答案為 "No"。
    
    # 計算需要的格子數
    from math import ceil
    n_x = (w + a - 1) // a
    n_y = (h + b - 1) // b
    
    # 判斷 x1, x2 是否為 a 的整數倍
    if x1 % a != 0 or x2 % a != 0:
        return "No"
    # 判斷 y1, y2 是否為 b 的整數倍
    if y1 % b != 0 or y2 % b != 0:
        return "No"
    
    # 取得格子座標
    i1, j1 = x1 // a, y1 // b
    i2, j2 = x2 // a, y2 // b
    
    # 兩塊板材不重疊，題目已保證
    # 判斷格子座標是否在範圍內（可超出屋頂邊界，但必須在格子範圍內）
    # 由於板材可以超出屋頂邊界，格子座標可以是負數或大於 n_x-1, n_y-1
    # 但我們只要能用 n_x * n_y 格子覆蓋屋頂，且兩塊板材在格子中即可
    # 因此不需限制 i1, i2, j1, j2 在 0..n_x-1, 0..n_y-1
    
    # 因此，只要 x1,x2 是 a 的倍數，y1,y2 是 b 的倍數即可覆蓋
    return "Yes"

t = int(input())
for _ in range(t):
    w, h, a, b = map(int, input().split())
    x1, y1, x2, y2 = map(int, input().split())
    print(can_cover(w, h, a, b, x1, y1, x2, y2))