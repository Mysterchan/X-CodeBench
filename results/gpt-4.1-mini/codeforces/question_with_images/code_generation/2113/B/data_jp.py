import sys
input = sys.stdin.readline

def can_tile(w, h, a, b, x1, y1, x2, y2):
    # 面積がa*bのシートで屋根を覆うには、屋根の面積がa*bの倍数である必要がある
    if (w * h) % (a * b) != 0:
        return False

    # 2枚のシートの左下座標から、それぞれのシートの範囲を求める
    # シートはサイズ a x b で回転不可
    # シートの範囲は [x_i, x_i + a) x [y_i, y_i + b)
    # すでに置かれている2枚のシートは重ならないことが保証されている

    # 屋根の範囲は [0, w) x [0, h)
    # シートは屋根の外にはみ出してもよい

    # 既に置かれている2枚のシートの領域を除いた屋根の領域を
    # a x b のシートで重ならずに埋められるかを判定する

    # 既に置かれている2枚のシートは取り除けないので、
    # それらのシートの領域は埋まっているとみなす

    # つまり、屋根の領域から2枚のシートの重なっている部分を除いた領域を
    # a x b のシートで埋める必要がある

    # しかし、シートは回転不可で、重なり不可、端接触可

    # 重要なポイントは、シートは屋根の境界を超えて伸びてよいこと
    # つまり、屋根の外側にシートを置いてもよい

    # したがって、屋根の中の空き領域をa x bのシートで埋めることができればよい

    # 2枚のシートの屋根内の重なり部分を計算し、屋根内の空き領域の面積を求める

    # まず、2枚のシートの屋根内の重なり部分の面積を計算
    def overlap_area(x1, y1, x2, y2):
        # 2つの矩形の重なり面積を計算
        # 矩形は [x, x+a) x [y, y+b)
        x_left = max(x1, x2)
        y_bottom = max(y1, y2)
        x_right = min(x1 + a, x2 + a)
        y_top = min(y1 + b, y2 + b)
        if x_right <= x_left or y_top <= y_bottom:
            return 0
        return (x_right - x_left) * (y_top - y_bottom)

    # 2枚のシートの屋根内の面積を計算
    def sheet_area_in_roof(x, y):
        # シートの屋根内の面積
        x_left = max(x, 0)
        y_bottom = max(y, 0)
        x_right = min(x + a, w)
        y_top = min(y + b, h)
        if x_right <= x_left or y_top <= y_bottom:
            return 0
        return (x_right - x_left) * (y_top - y_bottom)

    area1 = sheet_area_in_roof(x1, y1)
    area2 = sheet_area_in_roof(x2, y2)
    overlap = overlap_area(x1, y1, x2, y2)
    # 2枚のシートは重ならないことが保証されているので overlap == 0

    # 屋根の面積
    roof_area = w * h

    # 空き領域の面積
    empty_area = roof_area - (area1 + area2)

    # 空き領域は0以上でなければならない
    if empty_area < 0:
        return False

    # 空き領域をa x bのシートで埋めるには、空き領域がa*bの倍数である必要がある
    if empty_area % (a * b) != 0:
        return False

    # ここからが本質的な問題
    # 既に置かれている2枚のシートは固定されている
    # それらのシートの位置は任意で、屋根の外にはみ出している可能性もある
    # シートは回転不可で、重なり不可、端接触可
    # 追加で置くシートは屋根の外にはみ出してもよい

    # 追加で置くシートは屋根の空き領域を完全に覆う必要がある
    # つまり、屋根の空き領域はa x bのシートでタイル可能でなければならない

    # しかし、空き領域は複雑な形状になる可能性がある
    # だが、シートは屋根の外にはみ出してもよいので、
    # 空き領域の形状は問題にならない
    # 重要なのは、空き領域の幅と高さがaとbの倍数であるかどうか

    # そこで、空き領域の幅と高さを考える
    # 空き領域は屋根の矩形から2枚のシートの屋根内部分を除いた領域
    # 2枚のシートは重ならず、屋根内に部分的にかかっている

    # 2枚のシートの屋根内のx範囲の合計を考える
    # 2枚のシートのx範囲の合計は (x1_in_roof_width + x2_in_roof_width)
    # 同様にy範囲の合計も考える

    # しかし、これだけでは空き領域の形状はわからない

    # ここで、問題のヒントは、シートは回転不可で、
    # 既に置かれている2枚のシートは重ならず、
    # 追加のシートは屋根の外にはみ出してもよいこと

    # つまり、屋根の幅wをaで割ったときの余りと、
    # 屋根の高さhをbで割ったときの余りが重要

    # 既に置かれている2枚のシートのx座標の範囲を考える
    # 2枚のシートのx座標の最小値と最大値を求める
    x_min = min(x1, x2)
    x_max = max(x1 + a, x2 + a)
    y_min = min(y1, y2)
    y_max = max(y1 + b, y2 + b)

    # 2枚のシートのx方向の幅
    width_covered = x_max - x_min
    # 2枚のシートのy方向の幅
    height_covered = y_max - y_min

    # 2枚のシートの屋根内のx方向の重なりは0なので、
    # 2枚のシートの屋根内のx方向の合計幅は width_covered
    # 同様にy方向も height_covered

    # 追加のシートは屋根の外にはみ出してもよいので、
    # 屋根の幅wと高さhをa,bで割った余りが、
    # 2枚のシートのx,yの位置に影響されるかを考える

    # ここで、問題の本質は以下の2つの条件のいずれかを満たすかどうか
    # 1) 2枚のシートのx座標の差がaの倍数であり、
    #    かつ屋根の幅wがaの倍数である
    # 2) 2枚のシートのy座標の差がbの倍数であり、
    #    かつ屋根の高さhがbの倍数である

    # これらの条件を満たせば、既に置かれている2枚のシートを固定したまま、
    # 残りの空き領域をa x bのシートで埋めることができる

    # 具体的には、2枚のシートのx座標の差がaの倍数であれば、
    # x方向にaのグリッドが揃っていることになる
    # 同様にy座標の差がbの倍数であればy方向にbのグリッドが揃っていることになる

    # これにより、屋根全体をa x bのグリッドで覆うことが可能になる

    # したがって、以下の条件を判定する

    # x座標の差がaの倍数か
    dx = abs(x1 - x2)
    dy = abs(y1 - y2)

    cond_x = (dx % a == 0) and (w % a == 0)
    cond_y = (dy % b == 0) and (h % b == 0)

    return cond_x or cond_y

t = int(input())
for _ in range(t):
    w, h, a, b = map(int, input().split())
    x1, y1, x2, y2 = map(int, input().split())
    print("Yes" if can_tile(w, h, a, b, x1, y1, x2, y2) else "No")