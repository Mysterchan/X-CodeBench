    # Время выживания - количество ходов Доран, пока он не поймает Кругу.
    #
    # Анализ:
    # - Доран может двигаться по диагонали, значит его скорость равна max(|dr|, |dc|)
    # - Круга может двигаться только по вертикали/горизонтали, значит скорость равна |dr|+|dc|
    #
    # Игра начинается с хода Круги, затем Доран.
    #
    # Если Доран не может догнать Кругу, то ответ -1.
    #
    # Иначе время выживания - минимальное t, при котором Доран может гарантированно поймать Кругу.
