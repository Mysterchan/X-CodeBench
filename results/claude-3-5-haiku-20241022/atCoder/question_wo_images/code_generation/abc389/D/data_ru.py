R = int(input())

count = 0
R_sq = R * R

# Перебираем только неотрицательные значения i и j
# используя симметрию для подсчета всех квадрантов
for i in range(R + 1):
    for j in range(R + 1):
        # Проверяем самый дальний угол квадрата от центра
        # Для квадрата с центром в (i, j) углы находятся в:
        # (i+0.5, j+0.5), (i+0.5, j-0.5), (i-0.5, j+0.5), (i-0.5, j-0.5)
        
        # Самый дальний угол будет иметь максимальное расстояние
        max_dist_sq = (abs(i) + 0.5) ** 2 + (abs(j) + 0.5) ** 2
        
        if max_dist_sq <= R_sq:
            # Подсчитываем с учетом симметрии
            if i == 0 and j == 0:
                count += 1
            elif i == 0 or j == 0:
                count += 2
            else:
                count += 4

print(count)