n = int(input())
s = input()

# Найдем позиции всех единиц
ones = [i for i in range(n) if s[i] == '1']

# Количество единиц
k = len(ones)

# Если все единицы уже смежные, ответ 0
if ones[-1] - ones[0] + 1 == k:
    print(0)
else:
    # Нужно найти оптимальное окно размера k, которое минимизирует количество обменов
    # Количество обменов = количество нулей в окне
    # Эквивалентно: нужно найти окно размера (ones[-1] - ones[0] + 1 - k + len_window)
    # которое содержит все k единиц и минимум нулей между ними
    
    # На самом деле, нам нужно найти непрерывный отрезок длины k позиций,
    # куда мы хотим собрать все единицы. Стоимость = количество нулей в этом отрезке.
    
    min_cost = float('inf')
    
    # Перебираем все возможные окна размера k
    for start in range(n - k + 1):
        end = start + k - 1
        # Считаем количество нулей в окне [start, end]
        zeros = 0
        for i in range(start, end + 1):
            if s[i] == '0':
                zeros += 1
        min_cost = min(min_cost, zeros)
    
    print(min_cost)