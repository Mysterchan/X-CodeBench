import sys
input = sys.stdin.readline

T = int(input())
for _ in range(T):
    N = int(input())
    # Для каждого автора считаем:
    # Div1_i = min(A_i, B_i, C_i)
    # Div2_i = min(B_i, C_i, D_i)
    # Div3_i = min(C_i, D_i, E_i)
    # Максимальное количество C3C, используя первых k авторов:
    # X_k = sum_{i=1}^k (Div1_i + Div2_i + Div3_i)
    # Потому что задачи разных дивизионов не пересекаются (разные задачи),
    # и задачи разных авторов не пересекаются.
    # Каждое предложение можно использовать максимум в одном дивизионе одного C3C,
    # но так как мы считаем максимальное количество C3C, 
    # и каждый дивизион требует задачи от одного автора,
    # то максимальное количество C3C от автора i - это сумма Div1_i + Div2_i + Div3_i,
    # так как каждый дивизион требует разные задачи (разные типы),
    # и задачи не пересекаются по типам между дивизионами.
    # Следовательно, для первых k авторов максимальное количество C3C - сумма по ним.

    prefix = []
    total = 0
    for __ in range(N):
        A,B,C,D,E = map(int, input().split())
        div1 = min(A,B,C)
        div2 = min(B,C,D)
        div3 = min(C,D,E)
        total += div1 + div2 + div3
        prefix.append(total)
    print(*prefix)