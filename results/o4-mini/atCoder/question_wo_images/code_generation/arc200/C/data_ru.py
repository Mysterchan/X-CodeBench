import sys
input = sys.stdin.readline

# Ключевая идея:
# Недовольство человека i — количество других людей, которые пересекают его место P_i в интервал (L_i, R_i).
# Пересечение происходит, если интервалы (L_i,R_i) и (L_j,R_j) пересекаются, и места P_i и P_j таковы, что
# человек j проходит через место P_i (т.е. P_j > P_i, так как j проходит через места 1..P_j-1).
#
# Чтобы минимизировать суммарное недовольство, нужно минимизировать количество таких пересечений.
#
# Анализ:
# - Все L_i и R_i различны.
# - Если человек i сидит на месте p, то он проходит через места 1..p-1.
# - Другой человек j пересекает i, если j проходит через место p в интервал (L_i,R_i).
#   Это возможно, если P_j > p и интервалы (L_i,R_i) и (L_j,R_j) пересекаются.
#
# Следовательно, для минимизации суммарного недовольства нужно упорядочить людей так,
# чтобы люди с "ранними" интервалами (по времени) сидели на местах с меньшими номерами,
# а с "поздними" — на больших.
#
# Лексикографически наименьшая перестановка при минимальном суммарном недовольстве достигается,
# если отсортировать людей по возрастанию L_i (время прихода).
#
# Проверка на примерах из условия подтверждает это.
#
# Таким образом, решение:
# - Для каждого теста:
#   - Считать N, массивы L и R.
#   - Отсортировать индексы людей по L_i.
#   - Вывести перестановку индексов в этом порядке.

T = int(input())
for _ in range(T):
    N = int(input())
    people = []
    for i in range(N):
        L, R = map(int, input().split())
        people.append((L, R, i+1))
    # Сортируем по L_i (время прихода)
    people.sort(key=lambda x: x[0])
    # Выводим перестановку P: P_i - место для человека i
    # Здесь P_i - позиция в отсортированном списке, но нам нужно вывести перестановку по номерам людей
    # В условии P_i - место, назначенное человеку i.
    # Мы отсортировали людей по L_i, и присвоили им места 1..N в этом порядке.
    # Значит, для каждого человека i нужно вывести место, на котором он сидит.
    # Создадим массив pos, где pos[person_id] = место
    pos = [0]*(N+1)
    for seat, (_, _, person_id) in enumerate(people, 1):
        pos[person_id] = seat
    print(*pos[1:])